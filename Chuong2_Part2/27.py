n = int(input("Nhập số cạnh của hình: "))
if n == 3:
    print("Đây là hình tam giác.")
elif n == 4:
    print("Đây là hình tứ giác.")
elif n == 5:
    print("Đây là hình ngũ giác.")
elif n == 6:
    print("Đây là hình lục giác.")
elif n == 7:
    print("Đây là hình thất giác.")
elif n == 8:
    print("Đây là hình bát giác.")
elif n == 9:
    print("Đây là hình cửu giác.")
elif n == 10:
    print("Đây là hình thập giác.")
else:
    print("Lỗi: Chương trình chỉ hỗ trợ các hình có từ 3 đến 10 cạnh.")
